function sayHello(name) {
  return `Hello, ${name}`;
}

console.log(`Answer: ${sayHello("Mr. Spock")}`, "Result: Hello, Mr. Spock");
console.log(`Answer: ${sayHello("Captain Kirk")}`, "Result: Hello, Captain Kirk");
console.log(`Answer: ${sayHello("Liutenant Uhura")}`, "Result: Hello, Liutenant Uhura");
console.log(`Answer: ${sayHello("Dr. McCoy")}`, "Result: Hello, Dr. McCoy");
